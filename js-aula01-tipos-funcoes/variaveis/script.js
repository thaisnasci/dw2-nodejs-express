// Variaveis podem ser declarada de 3 formas:
// VAR,LET e CONST

// VAR: No geral evite seu uso, pode não ser muito seguro.
// LET: Utilize quando for necessário reatribuir o valor da variavel.
// CONST:Utilize quando não precisar reatribuir valor da variavel.

var cidade = "Registro";
var cidade = "Sete Barras";
document.write(cidade);

let nome = "Diego";
nome = "Thais";
document.write("<br>");
document.write(nome);

const pais = "Brasil";
pais = "Argentina";
//O valor de uma const não pode ser alterado"
document.write("<br>");
document.write(pais);